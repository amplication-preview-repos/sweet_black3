import React, { useEffect, useState } from "react";
import { Admin, DataProvider, Resource } from "react-admin";
import buildGraphQLProvider from "./data-provider/graphqlDataProvider";
import { theme } from "./theme/theme";
import Login from "./Login";
import "./App.scss";
import Dashboard from "./pages/Dashboard";
import { LocationList } from "./location/LocationList";
import { LocationCreate } from "./location/LocationCreate";
import { LocationEdit } from "./location/LocationEdit";
import { LocationShow } from "./location/LocationShow";
import { StatusUpdateList } from "./statusUpdate/StatusUpdateList";
import { StatusUpdateCreate } from "./statusUpdate/StatusUpdateCreate";
import { StatusUpdateEdit } from "./statusUpdate/StatusUpdateEdit";
import { StatusUpdateShow } from "./statusUpdate/StatusUpdateShow";
import { BowlList } from "./bowl/BowlList";
import { BowlCreate } from "./bowl/BowlCreate";
import { BowlEdit } from "./bowl/BowlEdit";
import { BowlShow } from "./bowl/BowlShow";
import { UserList } from "./user/UserList";
import { UserCreate } from "./user/UserCreate";
import { UserEdit } from "./user/UserEdit";
import { UserShow } from "./user/UserShow";
import { jwtAuthProvider } from "./auth-provider/ra-auth-jwt";

const App = (): React.ReactElement => {
  const [dataProvider, setDataProvider] = useState<DataProvider | null>(null);
  useEffect(() => {
    buildGraphQLProvider
      .then((provider: any) => {
        setDataProvider(() => provider);
      })
      .catch((error: any) => {
        console.log(error);
      });
  }, []);
  if (!dataProvider) {
    return <div>Loading</div>;
  }
  return (
    <div className="App">
      <Admin
        title={"AnimalHelpService"}
        dataProvider={dataProvider}
        authProvider={jwtAuthProvider}
        theme={theme}
        dashboard={Dashboard}
        loginPage={Login}
      >
        <Resource
          name="Location"
          list={LocationList}
          edit={LocationEdit}
          create={LocationCreate}
          show={LocationShow}
        />
        <Resource
          name="StatusUpdate"
          list={StatusUpdateList}
          edit={StatusUpdateEdit}
          create={StatusUpdateCreate}
          show={StatusUpdateShow}
        />
        <Resource
          name="Bowl"
          list={BowlList}
          edit={BowlEdit}
          create={BowlCreate}
          show={BowlShow}
        />
        <Resource
          name="User"
          list={UserList}
          edit={UserEdit}
          create={UserCreate}
          show={UserShow}
        />
      </Admin>
    </div>
  );
};

export default App;
